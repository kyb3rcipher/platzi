product = "Oranges";
price = null;

switch (product) {
    case "Oranges":
        price = 20;
        break;
    
    case "Apples":
        price = 50;
        break;
    
    case "Bannas":
        price = 25;
        break;
    
    case "Berrys":
        price = 90;
        break;
}

console.log("The price is: " + price);